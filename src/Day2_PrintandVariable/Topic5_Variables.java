package Day2_PrintandVariable;

public class Topic5_Variables {

    public static void main(String[] args) {
        // variables are the containers where they hold the values for us.

        /*
        primitive Data Types:
        1) boolean==> holds 2 values, true or false
        2) byte==> holds the whole number in range of -128 to 127
        3) char ==> holds the characters for instance c, or R, or" or }
        4) shors==>holds whole number in range of -327768 to 327767
        5) int==> holds whole number
        6) flat==> holds decimal number up to 7 decimal digits
        7) double==> holds decimal number up to 16 decimal digit
         */

        //type name = value;
        boolean theName=true;
        System.out.println(theName);

        boolean defValue = false;
        System.out.println(defValue);

        //type name=value
        byte aNumber = 120;

        System.out.println(aNumber);

        //type name = value (for char values we use '' (single quotes) and always has to be a single value.
        char aChar = 'h';
        System.out.println(aChar);
    }
}
