package Day2_PrintandVariable;

public class Topic4_EscapeSequence {

    public static void main(String[] args) {
        // \t it is going to leave paragraph space on the console
        System.out.println("This is the line and if i want to put ptag space \tHello World");

        //\n its going to move on to the new line on the console

        System.out.println("This is a code where i go to next \nLine");

        //\" print double quote " on the console
        System.out.println("This is\"Hrakar\" ");

        //\' print single quote on the console
        System.out.println("He \'said \'I want to go home\'");
        System.out.println("\"\\This is Me\\\"");

        // I want to print \Hrakar\
        System.out.println("\\Hrakar\\");
        //  \t:paragraph space
        //  \n: new line
        //  \": prints double quote " on the console
        //  \\: prints \ on the console
        //  \': prints single quote
        //  ' : prints single quote

    }
}
