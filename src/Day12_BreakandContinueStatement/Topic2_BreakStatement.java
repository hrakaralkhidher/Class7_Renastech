package Day12_BreakandContinueStatement;

public class Topic2_BreakStatement {
    public static void main(String[] args) {
// break statements
        // we use the break statement in for loops to get out of the loop when certain condition are met.

        for (int i = 0; i < 10; i++) {
            System.out.println("this is a code line");
            break;
        }
        System.out.println("this is outside of the for loop");













    }//end main
}//end class
